<h2>Assassination</h2>
<p>The first skill tree the Nightblade has access to, <em>Assassination</em>, focuses on this very strategy, incorporating heavy hitting melee attacks and abilities designed to deliver high melee DPS. Assassination also gives a bit of avoidance, to help the Nightblade stay in the thick of the action for long enough to secure their kill.</p>
<ul>
	<li><strong>[Ultimate] Death Stroke</strong> - Deals damage to your target, and reduces their incoming healing. The damage dealt is increased by your Ultimate pool.</li>
	<li><strong>[Active] Assassin?s Blade</strong> - Deals moderate damage with a large damage multiplier when used against low health enemies.</li>
	<li><strong>[Active] Teleport Strike</strong> - Teleport directly to your target, dealing damage and stunning them for a short duration.</li>
	<li><strong>[Active] Blur</strong> - A medium duration self buff that gives attackers a percentage based miss chance when attacking the Nightblade.</li>
	<li><strong>[Active] Mark Target</strong> - Place a mark on a high-health target; while marked you deal extra damage to the target. Killing the marked target will heal the Nightblade.</li>
	<li><strong>[Active] Haste</strong> - Increases the attack speed of normal and power attacks for a moderate duration.</li>
	<li><strong>[Passive] Master Assassin</strong> - Deal increased damage while stealthed or invisible. Successful sneak attacks stun for longer.</li>
	<li><strong>[Passive] Executioner</strong> - Killing an enemy with an Assassination ability restores some Magicka.</li>
	<li><strong>[Passive] Pressure Points</strong> - Increases the Nightblade's critical strike chance for every Assassination ability on their current hotbar.</li>
	<li><strong>[Passive] Hemorrhage</strong> - Increases the Nightblade's bonus critical strike damage for each Assassination ability on their current hotbar.</li>
</ul>

<h2>Shadow</h2>
<p>Next up, the <em>Shadow</em> line provides some great utility to complement the assassin style. Shadow includes a nice bag of tricks, that can be used strategically to turn the course of a fight in the Nightblades' favor.</p>
<ul>
	<li><strong>[Ultimate] Consuming Darkness</strong> - An AoE snare that renders the Nightblade invisibile. Attacking an enemy removes the invisibility for a short time. Allies can activate the Slip Away synergy, also gaining invisibility.</li>
	<li><strong>[Active] Shadow Cloak</strong> - Gain invisibility for a short duration.</li>
	<li><strong>[Active] Veiled Strike</strong> - Deals increased damage and stun enemies. Requires Stealth.</li>
	<li><strong>[Active] Summon Shade</strong> - Summons a shade which attacks and debuffs your enemies causing them to deal less damage.</li>
	<li><strong>[Active] Path of Darkness</strong> - Deals damage in a path in front of you, increases your movement speed along that path.</li>
	<li><strong>[Active] Aspect of Terror</strong> - Nearby enemies are afflicted with Fear.</li>
	<li><strong>[Passive] Refreshing Shadow</strong> - Activating a Shadow ability increases stamina regeneration</li>
	<li><strong>[Passive] Shadow Barrier</strong> - Take less damage after coming out of stealth or invisibility</li>
	<li><strong>[Passive] Fortitude</strong> - Increase max Health per slotted Shadow ability slotted</li>
	<li><strong>[Passive] Dark Veil</strong> - Increase duration of Shadow abilities</li>
</ul>

<h2>Siphoning</h2>
<p><em>Siphoning</em> presents an alternative approach to the <em>Assassination</em> tree, rather than overwhelming a foe with burst damage, Siphoning focuses on prolonging fights and wearing your enemies down with DoT's, crowd control, and self-heals.</p>
<ul>
	<li><strong>[Ultimate] Soul Shred</strong> - Deals PBAoE damage and stuns nearby enemies. Initiates the Leeching Strike synergy.</li>
	<li><strong>[Active] Strife</strong> - Places a DoT on your target which also heals you for the duration.</li>
	<li><strong>[Active] Agony</strong> - Stuns and DoTs an enemy for a long duration, but the effect is broken by any other damage.</li>
	<li><strong>[Active] Cripple</strong> - Places a DoT on an enemy which gradually decreases its movement speed, transferring an equal speed bonus to the Nightblade.</li>
	<li><strong>[Active] Siphoning Strikes</strong> - Sacrifice a portion of your own weapon damage in to restore a portion of your Magicka and Stamina with every hit.</li>
	<li><strong>[Active] Drain Power</strong> - Debuff the attack power of nearby enemies, transferring a portion of that power to the Nightblade for each affected enemy.</li>
	<li><strong>[Passive] Catalyst</strong> - Increase potion effectiveness</li>
	<li><strong>[Passive] Magicka Flood</strong> - Increase maximum magicka when you have a Siphoning ability slotted.</li>
	<li><strong>[Passive] Soul Siphoner</strong> - Increases healing taken by Siphoning abilities</li>
	<li><strong>[Passive] Transfer</strong> - Gain ultimate when dealing damage with a Siphoning ability</li>
</ul>