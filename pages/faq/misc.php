	<div id="miscellaneous" class="faq-section">
		<header>
			<h2>Miscellaneous</h2>
		</header>
		<ul>
			<li>Players are prompted to choose both a first and last name for their character at creation. Character customization options include many sliders, options, and color choices allowing players to fine tune their avatar's body type, age, appearance, and build. There is no height adjustment in ESO.<sup><a href="#ref">(MA1)</a></sup></li>
			<li>Speechcraft exists in the game, and can be used to "short-circuit" or sidestep certain quest obstacles, this often opens up alternative means of completing quest objectives.<sup><a href="#ref">(GI8)</a></sup></li>
			<li>There will be a lockpicking minigame. Every character can lockpick to a basic degree, although high difficulty locks may require characters with sufficient finesse to open.<sup><a href="#ref">(TF1)</a></sup></li>
			<li>Lockpicks can be used to open chests of various difficulty levels. Each lockpicking attempt requires the player to have a lockpick in his inventory, but these picks can break if used unsuccessfully.<sup><a href="#ref">(GN1)</a></sup></li>
			<li>It is possible to become a werewolf or a vampire in TESO, this grants the player an additional ability which can trigger a transformation. While transformed, the player's hotbar is temporarily replaced with abilities specific to their werewolf or vampire form.<sup><a href="#ref">(TF1)</a></sup></li>
			<li>There are no long-duration NPC companions which are linked to the player.<sup><a href="#ref">(GF1)</a></sup></li>
			<li>Player and guild housing will not be in the game at release.<sup><a href="#ref">(GI1)</a></sup></li>
			<li>There is not a cosmetic outfit or armor dying system at launch, but there are some costuming options through the use of a "disguise" which changes the visual appearance of the full set of the user's equipment when worn in a special inventory slot. From a practical perspective, disguises can be used to bypass certain types of NPCs, but they may also be kept and worn purely for aesthetic appeal.<sup><a href="#ref">(GF1)</a></sup></li>
			<li>Unlike the single player games, merchants do not have a limited supply of goods or money with which to trade. There is also no bartering system, instead prices of commodities are fixed.<sup><a href="#ref">(IGN1)</a></sup></li>
			<li>The inhabitants of Tamriel will be fully voice acted. Quest dialogue takes place in a mini-cinematic dialogue window. Conversations with the denizens of Tamriel involve making dialogue choices that blend clues about quest objectives with bits of lore and flavor.<sup><a href="#ref">(TF1),(MA1)</a></sup></li>
			<li>There will be a "trading post" or auction house in ESO where players can sell items. Specific details of this system are unknown, but it is likely that the exchange of commodities will be restricted on a per-faction basis<sup><a href="#ref">(TF1)</a></sup></li>
			<li>The pickpocketing skill will not be a mechanic in ESO for various reasons, namely that it is a mechanic which is very fun for certain people, but not fun for everyone else.<sup><a href="#ref">(IDT2)</a></sup></li>
		</ul>
