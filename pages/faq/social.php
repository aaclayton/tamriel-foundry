	<div id="guilds" class="faq-section">
		<header>
			<h2>Player Guilds</h2>
		</header>
		<ul>
			<li>Players will be able to join multiple guilds, although each guild is restricted to a single faction.<sup><a href="#ref">(TF1)</a></sup></li>
			<li>Guild leaders will be able to register guild lists before the launch of the game, and import all their members en-masse when the game launches.<sup><a href="#ref">(TF1)</a></sup></li>
		</ul>
	</div>

	<div id="networks" class="faq-section">
		<header>
			<h2>Social Networks</h2>
		</header>
		<ul>
			<li>TESO features a groundbreaking level of social networking integration. The game will be linked to Facebook, Twitter, or other social media at the player's discretion. You will be able to import your friends, contacts, groups, and circles directly into TESO in order to preserve your existing social network and merge it into your game experience.<sup><a href="#ref">(TF1)</a></sup></li>
		</ul>
	</div>

	<div id="friends" class="faq-section">
		<header>
			<h2>Friends and Gameplay Preferences</h2>
		</header>
		<ul>
			<li>Because of the way the megaserver allocates players to game spaces, management of your friends list and guild connections will be very important. The game server will attempt to place you in a space with your friends and guild mates, or alternatively with other players who have specified similar gameplay preferences.<sup><a href="#ref">(TF1)</a></sup></li>
			<li>You can answer a questionairre before launch (which you can modify anytime) that specifies your own gameplay preferences. Some of the many options you can specifically request are age bracket, roleplaying, looking for guild, or soloing. The megaserver will allocate you to a game space depending on these preferences, so all roleplayers would be grouped within an instance of the world. The system is intelligent, and learns through your preferences and actions where best to place each player. This creates a pseudo-server experience, in which players will begin to recognize and identify others with whom they are frequently placed.<sup><a href="#ref">(TF1)</a></sup></li>
			<li>ZeniMax wants to make it incredibly easy to play with your friends in TESO. There is an integrated group formation and management panel in the social window that lets you join up with your friends and teleport to them in the world (not in Cyrodiil).<sup><a href="#ref">(TF1)</a></sup></li>
			<li>ZeniMax does not have plans for a mentoring system at launch, but players can always revisit old zones to help friends with content.<sup><a href="#ref">(MR2)</a></sup></li>
		</ul>
	</div>
