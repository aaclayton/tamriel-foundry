			<h2>Grouping</h2>
		</header>
		<ul>
			<li>Groups in The Elder Scrolls Online consist of four players from the same alliance.<sup><a href="#ref">(GF1)</a></sup></li>
			<li>When grouped players make different storyline decisions in a quest, each individual will view the story as they chose it, rather than one person's choice affecting the entire group based on a random roll or draw.<sup><a href="#ref">(IGN1)</a></sup></li>
		</ul>
